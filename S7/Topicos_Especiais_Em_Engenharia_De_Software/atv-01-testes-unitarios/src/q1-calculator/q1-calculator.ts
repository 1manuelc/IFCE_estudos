export class Calculator {
	sum(a: number, b: number) {
		return a + b;
	}

	subtract(a: number, b: number) {
		return a - b;
	}

	multiply(a: number, b: number) {
		return a * b;
	}

	divide(a: number, b: number) {
		return a / b;
	}

	power(a: number, b: number) {
		return a ** b;
	}

	sqrt(a: number) {
		return Math.sqrt(a);
	}
}
